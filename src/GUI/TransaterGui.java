    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            SinText.setText("");
            if (EnText.getText().equals("") ^ EnText.getText() == null) {
                JOptionPane.showMessageDialog(rootPane, "Enter English Text");
            } else if (EnText.getText().equals("I am Janaka Pathma Kumara.I am Software Engineer\nStudent.Please Click Translate Button")) {
                SinText.setText(tr.sinhalaTranslate("I am Janaka Pathma Kumara.I am Software Engineer Student"));
            } else {
                SinText.setText(tr.sinhalaTranslate(EnText.getText()));
            }
        } catch (SQLException ex) {
            Logger.getLogger(TransaterGui.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(TransaterGui.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

