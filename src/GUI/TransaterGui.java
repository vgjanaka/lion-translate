    JFileChooser jf;
    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            if (SinText.getText().equals("")) {
                JOptionPane.showMessageDialog(rootPane, "Some Error Found");
            } else {
                if (jf == null) {
                    jf = new JFileChooser();
                }
                int i = jf.showSaveDialog(rootPane);
                if (i == 0) {
                    String savepath = jf.getSelectedFile().getPath().replace("\\", "/") + ".txt";
                    Writer out = new BufferedWriter(new OutputStreamWriter(new FileOutputStream(savepath), "UTF-8"));
                    try {
                        out.write(SinText.getText());
                    } finally {
                        out.close();
                    }
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_jButton4ActionPerformed
